package Entities;




public class Annexe {
    
    int idannexe;
    String codeannexe;
    String abrevAnnexe;
    String libelle;
    String periodicite;

    

    public int getIdannexe() {
        return idannexe;
    }

    public void setCodeannexe(String codeannexe) {
        this.codeannexe = codeannexe;
    }

    public String getCodeannexe() {
        return codeannexe;
    }

    public void setLibelle(String libelle) {
        this.libelle = libelle;
    }

    public String getLibelle() {
        return libelle;
    }

    public void setAbrevAnnexe(String abrevAnnexe) {
        this.abrevAnnexe = abrevAnnexe;
    }

    public String getAbrevAnnexe() {
        return abrevAnnexe;
    }

    public void setPeriodicite(String periodicite) {
        this.periodicite = periodicite;
    }

    public String getPeriodicite() {
        return periodicite;
    }

    public void setIdannexe(int idannexe) {
        this.idannexe = idannexe;
    }
}
